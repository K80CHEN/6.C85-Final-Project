    <p class="myth-title">Myth #2 Remittances are a form of aid.</p>
  </div>
  <div class="text-block">
    <p class="body-text">
      It is an unfortunate reality that in contemporary society, a common
      misconception persists that migration to the United States is a
      straightforward endeavor. Furthermore, migrants are often viewed through a
      negative lens, where they are scapegoated as the root cause of various
      economic and societal issues. Specifically, migrants are believed to be a
      drain on the American economy, absorbing resources without contributing to
      the nation's progress. However, this portrayal is fundamentally flawed and
      unjustified. It is crucial to examine the truth of the matter and shed
      light on the actual experiences of migrants in the US.
    </p>
    <p class="body-text">
      In a bid to force Mexico to fund the construction of the border wall,
      former President Trump threatened to cut off remittance payments from
      Mexican migrants in the US. This notion perpetuates the myth that
      remittances serve as a form of welfare for impoverished families in
      Central America, implying that these families rely solely on handouts
      without contributing to their own livelihoods.
    </p>
    <p class="body-text">
      A closer look at the World Food Program survey data, however, paints a
      different picture. It reveals that recipient families are actively
      working, with remittances constituting, on average, only x% of their
      household income. Furthermore, people should keep in mind that most
      families who receive remittances only allocate this money on basic needs
      to alleviate their financial constraints. See below a typical budget
      allocation for a Honduran family.
    </p>

    <p class="body-text">
      Additionally, it's important to highlight that while most recipient
      families may not be wealthy, they are not the poorest of the poor either.
      The high costs of international migration often prevent the poorest
      households from migrating and sending cross-border remittances.
      Consequently, remittances should not be seen as a substitute for
      government assistance, as they don't reach everyone, especially the most
      vulnerable populations.
    </p>
